public class Strings {
    public static void main(String[] args){
        String mystr = "An apple a day keeps doctor away";
        String mystr2 = "-By me";
        int myint = 4;
        System.out.println(mystr);
        System.out.println(mystr.length());
        System.out.println(mystr.toUpperCase());
        System.out.println(mystr.toLowerCase());
        System.out.println(mystr.indexOf(" "));//takes values as well as whitespace in the form of string
        System.out.println(mystr.concat(mystr2));
        System.out.println("\"Charles Darwin\"");
        System.out.println(mystr+myint);




    }
    
}